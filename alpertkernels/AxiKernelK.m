function Id = AxiKernelK(y1, y2, x1, x2, N1, N2)
% (N1,N2) is the normal of (y1,y2)
% purely numerical
%
% Hai 05/07/20

n = length(y1);
Id = zeros(n,4);

if (abs(x1) < 10^-14)
    % panel quadrature probably doesn't need this special case...
    keyboard
end

% p = 16; N = 32*p;
% tol = 1e-07;
% [tt,ww] = weight_setup(p,N, tol);
[tt, ww] = weight_setup(20, 32*20, 1e-10);

for k = 1:n
    a = y1(k); b = x1; c = y2(k) - x2; n1 = N1(k); n2 = N2(k);
    
    if sqrt((a-b)^2+c^2) < 1e-01
        f1 = @(t) (a-b*cos(t)).*(a*cos(t)-b).*( -b*n1*cos(t)+(a*n1+c*n2) )./(b^2+a^2-2*b*a*cos(t)+c^2).^(5/2);
        f2 = @(t) c*(a*cos(t)-b).*( -b*n1*cos(t)+(a*n1+c*n2) )./(b^2+a^2-2*b*a*cos(t)+c^2).^(5/2);
        f3 = @(t) c*(a-b*cos(t)).*( -b*n1*cos(t)+(a*n1+c*n2) )./(b^2+a^2-2*b*a*cos(t)+c^2).^(5/2);
        f4 = @(t) c^2*( -b*n1*cos(t)+(a*n1+c*n2) )./(b^2+a^2-2*b*a*cos(t)+c^2).^(5/2);

        Id(k,1) = sum(f1(tt).*ww);
        Id(k,2) = sum(f2(tt).*ww);
        Id(k,3) = sum(f3(tt).*ww);
        Id(k,4) = sum(f4(tt).*ww);
    else
        P = sqrt(a^2+b^2+2*a*b+c^2); Q = 4*a*b/P^2; M = sqrt(a^2+b^2-2*a*b+c^2);
    
        [K, E] = nellipke(Q);

        Id(k,1) = ((n1*b^2*a)*4*P/(2*a*b)^3/(24*(-1+Q)^2)*( 2*(-32+Q*(64+Q*(-33+Q+Q^2))))+( -b*n1*(b^2+a^2)-b*a*(a*n1+c*n2) )/P/(a*b)^2*( 1/(12*(-1+Q)^2)*(-2*(4-6*Q+Q^3))) ...
                    + ( b^2*n1*a+(a*n1+c*n2)*(b^2+a^2) )*4/(P^3*2*a*b)*(1/(6*(-1+Q)^2)*( 2*(1+(-1+Q)*Q))) -( b*a*(a*n1+c*n2) )*4/(P^5)*( 1/(3*(-1+Q)^2)*( -2*(-2+Q))))*E ...
                +((n1*b^2*a)*4*P/(2*a*b)^3/(24*(-1+Q)^2)*(-(-2+Q)*(-1+Q)*(-32+32*Q+Q^2)) + ( -b*n1*(b^2+a^2)-b*a*(a*n1+c*n2) )/P/(a*b)^2*( 1/(12*(-1+Q)^2)*((-1+Q)*(-8+8*Q+Q^2))) ...
                    + ( b^2*n1*a+(a*n1+c*n2)*(b^2+a^2) )*4/(P^3*2*a*b)*(1/(6*(-1+Q)^2)*(-(-2+Q)*(-1+Q))) -( b*a*(a*n1+c*n2) )*4/(P^5)*( 1/(3*(-1+Q)^2)*((-1+Q))))*K ;    

        Id(k,2) = ( -c*b*a*n1/P/(a*b)^2*( 1/(12*(-1+Q)^2)*( -2*(4-6*Q+Q^3) ) ) + c*( b^2*n1+a^2*n1+a*c*n2 )*4/(P^3*2*a*b)*(1/(6*(-1+Q)^2)*( 2*(1+(-1+Q)*Q) )) + c*( -b*(a*n1+c*n2) )*4/(P^5)*( 1/(3*(-1+Q)^2)*( -2*(-2+Q))))*E ...
                +( -c*b*a*n1/P/(a*b)^2*( 1/(12*(-1+Q)^2)*((-1+Q)*(-8+8*Q+Q^2))) + c*( b^2*n1+a^2*n1+a*c*n2 )*4/(P^3*2*a*b)*(1/(6*(-1+Q)^2)*(- (-2+Q)*(-1+Q) )) + c*( -b*(a*n1+c*n2) )*4/(P^5)*( 1/(3*(-1+Q)^2)*( (-1+Q))))*K;

        Id(k,3) = ( c*b^2*n1/P/(a*b)^2*( 1/(12*(-1+Q)^2)*( -2*(4-6*Q+Q^3)) )+c*( -b*a*n1-b*(a*n1+c*n2) )*4/(P^3*2*a*b)*(1/(6*(-1+Q)^2)*( 2*(1+(-1+Q)*Q)))+c*( a*(a*n1+c*n2) )*4/(P^5)*( 1/(3*(-1+Q)^2)*( -2*(-2+Q)) ))*E ...
                +( c*b^2*n1/P/(a*b)^2*( 1/(12*(-1+Q)^2)*((-1+Q)*(-8+8*Q+Q^2)))+c*( -b*a*n1-b*(a*n1+c*n2) )*4/(P^3*2*a*b)*(1/(6*(-1+Q)^2)*(-(-2+Q)*(-1+Q)))+c*( a*(a*n1+c*n2) )*4/(P^5)*( 1/(3*(-1+Q)^2)*((-1+Q))))*K;

        Id(k,4) =  (-b*n1*c^2*4/(P^3*2*a*b)*(1/(6*(-1+Q)^2)*( 2*(1+(-1+Q)*Q)))+(a*n1+c*n2)*c^2*4/(P^5)*( 1/(3*(-1+Q)^2)*( -2*(-2+Q))))*E ...
                + (-b*n1*c^2*4/(P^3*2*a*b)*(1/(6*(-1+Q)^2)*( - (-2+Q)*(-1+Q) )) + (a*n1+c*n2)*c^2*4/(P^5)*( 1/(3*(-1+Q)^2)*( (-1+Q))))*K;

    end    
    
end
% T(:,1)=T(:,4); T(:,2)=T(:,4); T(:,3)=T(:,4); 
Id = -repmat(y1,1,4).*Id;


end